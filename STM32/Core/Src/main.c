        HAL_UART_Transmit(&huart1, (uint8_t*)uart_buf, len, 100);

        // 6. Prosta logika sterowania na podstawie najwyższego prawdopodobieństwa
        if (results[0] > 0.8f) {
            HAL_UART_Transmit(&huart1, (uint8_t*)"WYKRYTO GEST 0\r\n", 16, 100);
        } 
